#include "random.h"

// Функція для ініціалізації генератора випадкових чисел
void init_random(void) {
    srand(time(NULL)); // Ініціалізація генератора випадкових чисел на основі поточного часу
}

// Функція для генерування випадкових чисел в діапазоні від 0 до 4095
int random_in_range(void) {
    // init_random();
    return rand() % 4096; // Генерація випадкового числа від 0 до 4095
}

